from wordcloud import WordCloud

# Load the csv file I uploaded
my_data = pd.read_csv('customer_reviews (1).csv')

# Checking if the data loaded correctly
print("Data loaded! Here are the first few rows:")
print(my_data.head())

# I am making two lists of words to help the computer understand the reviews
positive_words = ['great', 'excellent', 'love', 'fast', 'perfect', 'good', 'best', 'happy', 'amazing', 'nice']
negative_words = ['late', 'crack', 'cheap', 'worst', 'broke', 'waste', 'bad', 'terrible', 'broken', 'slow']

print("Word lists created!")

# This function looks at a review and decides if it is Good, Bad, or Okay
def check_feedback(review_text):
    review_text = str(review_text).lower() # make it lowercase

    score = 0
    # check for positive words
    for word in positive_words:
        if word in review_text:
            score = score + 1

    # check for negative words
    for word in negative_words:
        if word in review_text:
            score = score - 1

    # tell me the result
    if score > 0:
        return 'Happy'
    elif score < 0:
        return 'Unhappy'
    else:
        return 'Neutral'

