good_words = ['great', 'excellent', 'love', 'fast', 'perfect', 'good', 'best', 'happy', 'amazing', 'nice', 'smooth']
bad_words = ['late', 'crack', 'cheap', 'worst', 'broke', 'waste', 'bad', 'terrible', 'broken', 'slow', 'fragile']

# --- STEP 3: THE ANALYSIS LOGIC ---
# This function reads each review and looks for our keywords
def analyze_review(text):
    text = str(text).lower()
    score = 0

    # Add points for good words
    for word in good_words:
        if word in text:
            score = score + 1

    # Subtract points for bad words
    for word in bad_words:
        if word in text:
            score = score - 1

    if score > 0:
        return 'Satisfied'
    elif score < 0:
        return 'Dissatisfied'
    else:
        return 'Neutral'

# Applying the logic to our data
